package com.techieshashank.newsapp;

import android.content.Context;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.ArrayAdapter;
import android.widget.TextView;

import java.util.ArrayList;

public class NewsAdapter extends ArrayAdapter<News> {

    public NewsAdapter(Context context, ArrayList<News> news) {
        super(context, 0, news);
    }

    @Override
    public View getView(int position, View convertView, ViewGroup parent) {
        View listItemView = convertView;
        if (listItemView == null) {
            listItemView = LayoutInflater.from(getContext()).inflate(
                    R.layout.news_list_item, parent, false);
        }

        News currentNews = getItem(position);
        TextView newsTitleTextView = listItemView.findViewById(R.id.title);
        String title = currentNews.getTitle();
        newsTitleTextView.setText(title);


        TextView categoryView = listItemView.findViewById(R.id.category_text_view);
        String category = currentNews.getCategory();
        categoryView.setText(category);

        TextView dateView = listItemView.findViewById(R.id.date_text_view);
        String date = currentNews.getDate();
        dateView.setText(date);

        TextView authorView = listItemView.findViewById(R.id.author);
        String author = currentNews.getAuthor();
        authorView.setText(author);

        return listItemView;
    }
}